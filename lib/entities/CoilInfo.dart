class CoilInfo {
  String coilName;
  String coilDesc;
  String coilType;
  double primaryResonantFrequency;
  double secondaryResonantFrequency;

  CoilInfo({
    this.coilName = "",
    this.coilDesc = "",
    this.coilType = "",
    this.primaryResonantFrequency = 0.0,
    this.secondaryResonantFrequency = 0.0,
  });
}
